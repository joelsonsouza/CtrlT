    private void jTableDestinosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableDestinosMouseClicked
        String descricao = ""+jTableDestinos.getValueAt(jTableDestinos.getSelectedRow(), 1);

        conex.executaSql("select id_destino, descricao from tbl_destinos where descricao ilike '%"+descricao+"%'");
        try {
            conex.rs.first();
            modDest.setCodigo(conex.rs.getInt("id_destino"));
            modDest.setDescricao(conex.rs.getString("descricao"));

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Destino não encontrado"+ex);
        }

        jTextFieldCod.setText(String.valueOf(modDest.getCodigo()));
        jTextFieldDescricao.setText(modDest.getDescricao());

        conex.desconecta();
        jInternalFrameBuscaDestino.dispose();
        jButtonApagar.setEnabled(true);
        jButtonEditar.setEnabled(true);
        jButtonIncluir.setEnabled(false);
    }//GEN-LAST:event_jTableDestinosMouseClicked


    private void jButtonIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIncluirActionPerformed
       if(jTextFieldDescricao.getText().isEmpty()) {
          jLabelDescObgt.setVisible(true);
          JOptionPane.showMessageDialog(null, "Campo Obrigatório");
          jTextFieldDescricao.requestFocus();
        }else {
          modDest.setDescricao(jTextFieldDescricao.getText());
          control.Salvar(modDest);
          
            modDest.setDescricao(jTextFieldDescricao.getText());
            BeansDestinos model = control.Pesquisar(modDest);
            jTextFieldCod.setText(String.valueOf(model.getCodigo()));
          
          jLabelDescObgt.setVisible(false);
          jButtonIncluir.setEnabled(false);
          jButtonEditar.setEnabled(true);
          jButtonApagar.setEnabled(true);   
        }
    }//GEN-LAST:event_jButtonIncluirActionPerformed

    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        if (jTextFieldDescricao.getText().length() > 0) { 
                modDest.setCodigo(Integer.parseInt(jTextFieldCod.getText()));
                modDest.setDescricao(jTextFieldDescricao.getText());
                control.Editar(modDest);
                jLabelDescObgt.setVisible(false);
            }
            else {
                jLabelDescObgt.setVisible(true);
                JOptionPane.showMessageDialog(null, "Campo Obrigatório");
                jTextFieldDescricao.requestFocus();    
            }   
    }//GEN-LAST:event_jButtonEditarActionPerformed


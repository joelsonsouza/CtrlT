    private void jButtonApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonApagarActionPerformed
        int Confirm = JOptionPane.showConfirmDialog(null,"Excluir registro?"," Excluir", JOptionPane.YES_NO_OPTION);
            if (Confirm == JOptionPane.YES_OPTION) {
            modDest.setCodigo(Integer.parseInt(jTextFieldCod.getText()));
            modDest.setDescricao(jTextFieldDescricao.getText());
            control.Excluir(modDest);
            
            jTextFieldDescricao.setText("");
            jTextFieldCod.setText("");
            jButtonEditar.setEnabled(false);
            jButtonApagar.setEnabled(false);
            jLabelDescObgt.setVisible(false);
            jButtonIncluir.setEnabled(true);
            }else if (Confirm == JOptionPane.NO_OPTION){
                setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
            }
    }//GEN-LAST:event_jButtonApagarActionPerformed

    
    public void PreencherTabelaIF(String SQL){
      ArrayList dados = new ArrayList();
      String[] colunas = new String[]{"COD","DESTINO"};
      
      conex.conexao();
      conex.executaSql(SQL);

      try{
          conex.rs.first();       
          do{
              dados.add(new Object[]{conex.rs.getInt("id_destino"),
                                     conex.rs.getString("descricao")
                                   
                                    });
 
          }while(conex.rs.next());
      
        }catch(SQLException ex){
          JOptionPane.showMessageDialog(null, "Erro ao preencher ArrayList\nErro:"+ex);
        }
          
     BeansTabelas modelo = new BeansTabelas(dados, colunas);
      
     jTableDestinos.setModel(modelo);
     jTableDestinos.getColumnModel().getColumn(0).setPreferredWidth(45);
     jTableDestinos.getColumnModel().getColumn(0).setResizable(true);
     jTableDestinos.getColumnModel().getColumn(0).setHeaderValue("COD");
     jTableDestinos.getColumnModel().getColumn(1).setPreferredWidth(342);
     jTableDestinos.getColumnModel().getColumn(1).setResizable(true); //não redimensionaveL
     jTableDestinos.getColumnModel().getColumn(1).setHeaderValue("DESTINO");
           

     jTableDestinos.getTableHeader().setReorderingAllowed(false); //não reordenavel
     jTableDestinos.setAutoResizeMode(jTableDestinos.AUTO_RESIZE_OFF); //não redmencionar
     jTableDestinos.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);//selicioar um dado por vez
    }
    

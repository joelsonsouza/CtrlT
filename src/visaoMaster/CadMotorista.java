    public void PreencherTabelaIF(String SQL){
      ArrayList dados = new ArrayList();
      String[] colunas = new String[]{"",""};
      
      conex.conexao();
      conex.executaSql(SQL);

      try{
          conex.rs.first();       
          do{
              dados.add(new Object[]{conex.rs.getInt("id_motorista"),
                                     conex.rs.getString("nomemotorista")
                                   
                                    });
 
          }while(conex.rs.next());
      
        }catch(SQLException ex){
          JOptionPane.showMessageDialog(null, "Erro ao preencher ArrayList\nErro:"+ex);
        }
          
     BeansTabelas modelo = new BeansTabelas(dados, colunas);
      
     jTableMotorista.setModel(modelo);
     jTableMotorista.getColumnModel().getColumn(0).setPreferredWidth(45);
     jTableMotorista.getColumnModel().getColumn(0).setResizable(true);
     jTableMotorista.getColumnModel().getColumn(0).setHeaderValue("COD");
     jTableMotorista.getColumnModel().getColumn(1).setPreferredWidth(450);
     jTableMotorista.getColumnModel().getColumn(1).setResizable(true); //nÃ£o redimensionaveL
     jTableMotorista.getColumnModel().getColumn(1).setHeaderValue("NOME");
           

     jTableMotorista.getTableHeader().setReorderingAllowed(false); //nÃ£o reordenavel
     jTableMotorista.setAutoResizeMode(jTableMotorista.AUTO_RESIZE_OFF); //nÃ£o redmencionar
     jTableMotorista.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);//selicioar um dado por vez
    }

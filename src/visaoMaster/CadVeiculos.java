    public void PreencherTabelaIF(String SQL){
      ArrayList dados = new ArrayList();
      String[] colunas = new String[]{"COD","PLACA","MODELO","STATUS"};
      
      conex.conexao();
      conex.executaSql(SQL);

      try{
          conex.rs.first();       
          do{
              dados.add(new Object[]{conex.rs.getInt("id_veiculo"),
                                     conex.rs.getString("placa"),
                                     conex.rs.getString("descricao"),
                                     conex.rs.getBoolean("ativo")
                                   
                                    });
 
          }while(conex.rs.next());
      
        }catch(SQLException ex){
          JOptionPane.showMessageDialog(null, "Erro ao preencher ArrayList\nErro:"+ex);
        }
          
     BeansTabelas modelo = new BeansTabelas(dados, colunas);
      
     jTableVeiculo.setModel(modelo);
     jTableVeiculo.getColumnModel().getColumn(0).setPreferredWidth(40);
     jTableVeiculo.getColumnModel().getColumn(0).setResizable(false);
     jTableVeiculo.getColumnModel().getColumn(0).setHeaderValue("COD");
     jTableVeiculo.getColumnModel().getColumn(1).setPreferredWidth(80);
     jTableVeiculo.getColumnModel().getColumn(1).setResizable(false); //não redimensionaveL
     jTableVeiculo.getColumnModel().getColumn(1).setHeaderValue("PLACA");
     jTableVeiculo.getColumnModel().getColumn(2).setPreferredWidth(110);
     jTableVeiculo.getColumnModel().getColumn(2).setResizable(false); //não redimensionaveL
     jTableVeiculo.getColumnModel().getColumn(2).setHeaderValue("MODELO");
     jTableVeiculo.getColumnModel().getColumn(3).setPreferredWidth(70);
     jTableVeiculo.getColumnModel().getColumn(3).setResizable(false); //não redimensionaveL
     jTableVeiculo.getColumnModel().getColumn(3).setHeaderValue("STATUS");
           

     jTableVeiculo.getTableHeader().setReorderingAllowed(false); //não reordenavel
     jTableVeiculo.setAutoResizeMode(jTableVeiculo.AUTO_RESIZE_OFF); //não redmencionar
     jTableVeiculo.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);//selicioar um dado por vez
    }
    
    

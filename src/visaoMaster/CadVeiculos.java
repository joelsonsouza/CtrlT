    public void CbModelo(){
    
        conex.conexao();
        conex.executaSql("select *from tbl_modelo order by descricao");
        try{
            conex.rs.first();
            jComboBoxModelo.removeAllItems();
            do{
               jComboBoxModelo.addItem(conex.rs.getString("descricao"));
            }while(conex.rs.next());
            
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(rootPane, "Erro ao preecher dados");
        }
        conex.desconecta();      
    }

    private void jTableMarcaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMarcaMouseClicked
        String descricaomarca = ""+jTableMarca.getValueAt(jTableMarca.getSelectedRow(), 1);

        conex.executaSql("select id_marca, descricaomarca from tbl_marca where descricaomarca ilike '%"+descricaomarca+"%'");
        try {
            conex.rs.first();
            mod.setCodigo(conex.rs.getInt("id_marca"));
            mod.setDescricao(conex.rs.getString("descricaomarca"));

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Veículo não encontrado"+ex);
        }

        jTextFieldCod.setText(String.valueOf(mod.getCodigo()));
        jTextFieldDescricao.setText(mod.getDescricao());

        conex.desconecta();
        jInternalFrameBuscaMarca.dispose();
        jButtonApagar.setEnabled(true);
        jButtonEditar.setEnabled(true);
        jButtonIncluir.setEnabled(false);
    }//GEN-LAST:event_jTableMarcaMouseClicked


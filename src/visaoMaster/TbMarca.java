    public void PreencherTabelaIF(String SQL){
      ArrayList dados = new ArrayList();
      String[] colunas = new String[]{"COD","MARCA"};
      
      conex.conexao();
      conex.executaSql(SQL);

      try{
          conex.rs.first();       
          do{
              dados.add(new Object[]{conex.rs.getInt("id_marca"),
                                     conex.rs.getString("descricaomarca")
                                   
                                    });
 
          }while(conex.rs.next());
      
        }catch(SQLException ex){
          JOptionPane.showMessageDialog(null, "Erro ao preencher ArrayList\nErro:"+ex);
        }
          
     BeansTabelas modelo = new BeansTabelas(dados, colunas);
      
     jTableMarca.setModel(modelo);
     jTableMarca.getColumnModel().getColumn(0).setPreferredWidth(45);
     jTableMarca.getColumnModel().getColumn(0).setResizable(true);
     jTableMarca.getColumnModel().getColumn(0).setHeaderValue("COD");
     jTableMarca.getColumnModel().getColumn(1).setPreferredWidth(342);
     jTableMarca.getColumnModel().getColumn(1).setResizable(true); //não redimensionaveL
     jTableMarca.getColumnModel().getColumn(1).setHeaderValue("MARCA");
           

     jTableMarca.getTableHeader().setReorderingAllowed(false); //não reordenavel
     jTableMarca.setAutoResizeMode(jTableMarca.AUTO_RESIZE_OFF); //não redmencionar
     jTableMarca.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);//selicioar um dado por vez
    }
    

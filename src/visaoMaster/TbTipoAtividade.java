      public void PreencherTabelaIF(String SQL){
      ArrayList dados = new ArrayList();
      String[] colunas = new String[]{"COD","DESCRIÇÃO"};
      
      conex.conexao();
      conex.executaSql(SQL);

      try{
          conex.rs.first();       
          do{
              dados.add(new Object[]{conex.rs.getInt("id_tipoatividade"),
                                     conex.rs.getString("descricao")
                                   
                                    });
 
          }while(conex.rs.next());
      
        }catch(SQLException ex){
          //JOptionPane.showMessageDialog(null, "Erro ao preencher ArrayList\nErro:"+ex);
        }
          
     BeansTabelas modelo = new BeansTabelas(dados, colunas);
      
     jTableAtividade.setModel(modelo);
     jTableAtividade.getColumnModel().getColumn(0).setPreferredWidth(50);
     jTableAtividade.getColumnModel().getColumn(0).setResizable(true);
     jTableAtividade.getColumnModel().getColumn(0).setHeaderValue("COD");
     jTableAtividade.getColumnModel().getColumn(1).setPreferredWidth(345);
     jTableAtividade.getColumnModel().getColumn(1).setResizable(true); //não redimensionaveL
     jTableAtividade.getColumnModel().getColumn(1).setHeaderValue("DESCRIÇÃO");
           

     jTableAtividade.getTableHeader().setReorderingAllowed(false); //não reordenavel
     jTableAtividade.setAutoResizeMode(jTableAtividade.AUTO_RESIZE_OFF); //não redmencionar
     jTableAtividade.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);//selicioar um dado por vez
    }
    
    

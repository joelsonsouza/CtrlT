    private void jTableAtividadeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableAtividadeMouseClicked
       String atividade = ""+jTableAtividade.getValueAt(jTableAtividade.getSelectedRow(), 1);

        conex.executaSql("select id_tipoatividade, descricao from tbl_tipoatividade where descricao ilike '%"+atividade+"%'");
        try {
            conex.rs.first();
            mod.setCodigo(conex.rs.getInt("id_tipoatividade"));
            mod.setDescricao(conex.rs.getString("descricao"));
            

        } catch (SQLException ex) {
            //JOptionPane.showMessageDialog(null, "Veículo não encontrado");
        }

        jTextFieldCod.setText(String.valueOf(mod.getCodigo()));
        jTextFieldDescricao.setText(mod.getDescricao());
       
        conex.desconecta();
        jInternalFrameBuscaAtividade.dispose();
        jButtonApagar.setEnabled(true);
        jButtonEditar.setEnabled(true);
        jButtonIncluir.setEnabled(false);
    }//GEN-LAST:event_jTableAtividadeMouseClicked

